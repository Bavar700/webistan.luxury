// Current language
let currentLang = 'en';

function setLanguage(lang) {
    if (TRANSLATIONS[lang]) {
        currentLang = lang;
    }
    if (typeof document !== 'undefined') {
        const root = document.documentElement;
        if (root) {
            // Remove previous lang classes
            root.className = root.className.replace(/\blang-\S+/g, '');
            root.classList.add(`lang-${currentLang}`);
        }
    }
}

function getLanguage() {
    return currentLang;
}

/**
 * Get translated string by key
 * Supports {variable} placeholders
 * Usage: __('task.deadline') or __('test.result_reward', {score: 10, reward: 2})
 */
function __(key, params = null) {
    const langData = TRANSLATIONS[currentLang];
    let value = langData[key];

    // Fallback to Tajik if key missing in current language
    if (!value && currentLang !== 'tg') {
        value = TRANSLATIONS.tg[key];
    }

    if (!value) {
        console.warn(`Translation key not found: "${key}" for language "${currentLang}"`);
        return key;
    }

    if (params) {
        Object.keys(params).forEach(k => {
            value = value.replace(`{${k}}`, params[k]);
        });
    }

    return value;
}

/**
 * Get month name in current language
 */
function __month(index) {
    return (LANG_MONTHS[currentLang] || LANG_MONTHS.tg)[index] || LANG_MONTHS.tg[index] || '';
}

